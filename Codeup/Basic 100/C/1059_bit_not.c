// bit 단위 연산자 : ~(not), &(and), |(or), ^(xor), <<(left shift), >>(right shift)
// 00000000 00000000 00000000 00000001 : 1
// 11111111 11111111 11111111 11111110 : ~1 = -2

// 양의 정수는 2진수의 형태
