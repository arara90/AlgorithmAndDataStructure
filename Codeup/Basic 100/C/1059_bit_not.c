// 00000000 00000000 00000000 00000000 : 0
// 11111111 11111111 11111111 11111111 : -1 (0에서 -1 후, 32비트만 표시)
// 11111111 11111111 11111111 11111110 : -2
// 10000000 00000000 00000000 00000000 :  -2147483648 (최소)
// 01111111 11111111 11111111 11111111 : 2147483647 (최대)

// 10000000 00000000 00000000 00000000 :  -2147483648 (최소) 에서 -1?? 
// 01111111 11111111 11111111 11111111 : 2147483647??????? -> Overflow

// ~n = -n - 1 *******

//비트 단위로 1 -> 0, 0 -> 1로 바꾼 후 그 값을 10진수로 출력한다.
#include <stdio.h>
int main() 
{
    int a;
    scanf("%d", &a);
    printf("%d", ~a);
    return 0;
}